using namespace std;
void solve() {
    int n, w;
    cin >> n >> w;
    if (w == 1) {
        cout << 0 << endl;
        return;
    }
    if (n < w) {
        cout << n << endl;
        return;
    }
    int tem;
    if ((n + 1 - w) % w == 0)
        tem = (n + 1 - w) / w;
    else
        tem = (n + 1 - w) / w + 1;
    cout << n - tem << endl;
    return;
}
int main() {
    cin.tie(nullptr)->sync_with_stdio(false);
    int t;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}
